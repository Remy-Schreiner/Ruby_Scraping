def count_handles(handles)
  puts "Nombre de handles : #{handles.length}"
end

def shortest_handle(handles)
  puts "Handle le plus court : #{handles.min_by { |h| h.length }}"
end

def count_five_char(handles)
  puts "Nombre de handles avec 5 caractères : #{handles.count { |h| h[1..].length == 5 }}"
end

def count_uppercase(handles)
  puts "Nombre de handles commençant par une majuscule : #{handles.count { |h| h[1] =~ /[A-Z]/ }}"
end

def sorted_handles(handles)
  puts "Handles triés par ordre alphabétique :\n#{handles.sort.join(", ")}"
end

def sorted_by_size(handles)
  puts "Handles triés par taille :\n#{handles.sort_by(&:length).join(", ")}"
end

def epenser_position(handles)
  puts "Position de @epenser : #{handles.index('@epenser') || 'Non trouvé'}"
end

